function Vector3(x, y, z) {

    this.x = x || 0;
    this.y = y || 0;
    this.z = z || 0;

}

function Euler(x, y, z) {

    this.x = x || 0;
    this.y = y || 0;
    this.z = z || 0;

}

function Rect(x, y, w, h) {

    this.x = x || 0;
    this.y = y || 0;
    this.w = w || 0;
    this.h = h || 0;
}